<?php

namespace Aviron\UserBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * TypeLicence
 *
 * @ORM\Table(name="gestion_aviron_type_licence")
 * @ORM\Entity(repositoryClass="Aviron\UserBundle\Repository\TypeLicenceRepository")
 */
class TypeLicence
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="nom", type="string", length=25)
     */
    private $nom;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nom
     *
     * @param string $nom
     *
     * @return TypeLicence
     */
    public function setNom($nom)
    {
        $this->nom = $nom;

        return $this;
    }

    /**
     * Get nom
     *
     * @return string
     */
    public function getNom()
    {
        return $this->nom;
    }
}

